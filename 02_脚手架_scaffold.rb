一、资料
	必要资料：
	方案设计平面图：格式DWG，单位为米，有准确的坐标值，比例尺500、1000
	方案设计效果图：全局鸟瞰图（日景，两幅相对视角）、重要设计节点效果图

	可选资料：
	规划参数，其它特殊要求

二、制作、提交周期设定
	收到资料首先进行资料评估，确认资料完整度，评估时间不超过24小时。
	如果不完整将提出所缺部份，协调补充，直到资料完整。
	如果资料完整，能够满足制求要求，则回复确认邮件，同时开始计时。

	制作时间：
		4万平方米以下
		4万至10万平方米
		10万以上

	提交确认时间：
		制作完成后，需要进行客户确认，如有修改需求，需在48小时以内提出修改意见，否则默认为通过确认。
		收到修改需求后，会给出修改完成时间，立刻进行修改工作，
